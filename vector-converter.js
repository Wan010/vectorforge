// Main vector conversion and SVG generation
class VectorConverter {
    static generateSVG(tracingResult, options = {}) {
        const {
            optimizePaths = true,
            minify = true,
            includeMetadata = false,
            backgroundColor = '#ffffff'
        } = options;

        const { paths, originalSize } = tracingResult;
        
        // Generate SVG paths
        const svgPaths = this.generatePaths(paths, options);
        
        // Create SVG document
        const svgContent = this.createSVGDocument(svgPaths, originalSize, options);
        
        // Post-process SVG
        let finalSVG = svgContent;
        
        if (optimizePaths) {
            finalSVG = this.optimizeSVG(finalSVG);
        }
        
        if (minify) {
            finalSVG = this.minifySVG(finalSVG);
        }
        
        return finalSVG;
    }

    static generatePaths(paths, options) {
        return paths.map(path => {
            const attributes = [
                `d="${path.d}"`,
                `fill="${path.fill}"`,
                `stroke="${path.stroke}"`,
                `stroke-width="${path.strokeWidth}"`
            ];

            // Add opacity if present
            if (path.opacity !== undefined) {
                attributes.push(`opacity="${path.opacity}"`);
            }

            // Add class if present
            if (path.class) {
                attributes.push(`class="${path.class}"`);
            }

            return `<path ${attributes.join(' ')}/>`;
        }).join('\n    ');
    }

    static createSVGDocument(paths, size, options) {
        const { width, height } = size;
        const { backgroundColor = '#ffffff' } = options;

        const metadata = options.includeMetadata ? `
    <metadata>
        Generated by VectorMagic - Advanced Image to Vector Converter
        Date: ${new Date().toISOString()}
        Original Dimensions: ${width}x${height}
        Path Count: ${paths.split('</path>').length - 1}
    </metadata>` : '';

        const background = backgroundColor !== 'transparent' ? 
            `<rect width="100%" height="100%" fill="${backgroundColor}"/>` : '';

        return `<?xml version="1.0" encoding="UTF-8"?>
<svg width="${width}" height="${height}" viewBox="0 0 ${width} ${height}" 
     xmlns="http://www.w3.org/2000/svg" xmlns:xlink="http://www.w3.org/1999/xlink">
    ${metadata}
    ${background}
    ${paths}
</svg>`;
    }

    static optimizeSVG(svgString) {
        // Remove unnecessary whitespace and comments
        let optimized = svgString
            .replace(/<!--.*?-->/g, '') // Remove comments
            .replace(/\s+/g, ' ') // Collapse whitespace
            .replace(/> </g, '><') // Remove spaces between tags
            .trim();

        // Optimize path data (simplified)
        optimized = optimized.replace(
            /d="([^"]*)"/g,
            (match, pathData) => `d="${this.optimizePathData(pathData)}"`
        );

        return optimized;
    }

    static optimizePathData(pathData) {
        // Simple path optimization - remove redundant commands
        return pathData
            .replace(/([ML])\s+(\d+\.?\d*)\s+(\d+\.?\d*)\s+\1\s+(\d+\.?\d*)\s+(\d+\.?\d*)/g, '$1 $2 $3 $4 $5')
            .replace(/(\d)(?=\s*[ML])/g, '$1 ')
            .replace(/\s*,\s*/g, ' ')
            .replace(/\s+/g, ' ')
            .trim();
    }

    static minifySVG(svgString) {
        return svgString
            .replace(/\s*\n\s*/g, '')
            .replace(/\s+/g, ' ')
            .replace(/> </g, '><')
            .trim();
    }

    static generateCSS(svgElement, options = {}) {
        const cssRules = [];
        
        // Generate CSS for SVG elements
        cssRules.push(`
/* VectorMagic Generated CSS */
.svg-container {
    display: inline-block;
    max-width: 100%;
    height: auto;
}

.svg-container svg {
    width: 100%;
    height: auto;
}`);

        // Add animation keyframes if requested
        if (options.includeAnimations) {
            cssRules.push(`
@keyframes vectorFloat {
    0%, 100% { transform: translateY(0px); }
    50% { transform: translateY(-5px); }
}

.vector-path {
    animation: vectorFloat 3s ease-in-out infinite;
}`);
        }

        return cssRules.join('\n');
    }

    static estimateFileSize(svgString) {
        const blob = new Blob([svgString], { type: 'image/svg+xml' });
        return blob.size;
    }

    static createDownloadableSVG(svgString, filename = 'vector-image.svg') {
        const blob = new Blob([svgString], { type: 'image/svg+xml' });
        return {
            blob,
            url: URL.createObjectURL(blob),
            size: blob.size,
            filename
        };
    }

    static async convertToPNG(svgString, scale = 2) {
        return new Promise((resolve, reject) => {
            const img = new Image();
            const svgBlob = new Blob([svgString], { type: 'image/svg+xml' });
            const url = URL.createObjectURL(svgBlob);
            
            img.onload = () => {
                const canvas = document.createElement('canvas');
                canvas.width = img.width * scale;
                canvas.height = img.height * scale;
                
                const ctx = canvas.getContext('2d');
                ctx.imageSmoothingEnabled = true;
                ctx.imageSmoothingQuality = 'high';
                ctx.drawImage(img, 0, 0, canvas.width, canvas.height);
                
                URL.revokeObjectURL(url);
                resolve(canvas);
            };
            
            img.onerror = () => {
                URL.revokeObjectURL(url);
                reject(new Error('Failed to load SVG for PNG conversion'));
            };
            
            img.src = url;
        });
    }

    static async convertToJPG(canvas, quality = 0.9) {
        return new Promise((resolve) => {
            canvas.toBlob(resolve, 'image/jpeg', quality);
        });
    }

    static analyzeSVGComplexity(svgString) {
        const pathMatches = svgString.match(/<path/g) || [];
        const rectMatches = svgString.match(/<rect/g) || [];
        const circleMatches = svgString.match(/<circle/g) || [];
        
        const totalElements = pathMatches.length + rectMatches.length + circleMatches.length;
        
        // Estimate complexity based on file size and element count
        const fileSize = svgString.length;
        const complexityScore = (totalElements * fileSize) / 1000000;
        
        return {
            totalElements,
            pathCount: pathMatches.length,
            fileSize,
            complexityScore: Math.min(complexityScore, 1)
        };
    }
}

// Preset configurations for different use cases
class ConversionPresets {
    static getPreset(presetName) {
        const presets = {
            logo: {
                algorithm: 'edge',
                threshold: 100,
                smoothness: 8,
                noiseReduction: 2,
                cornerThreshold: 0.6,
                enableOptimization: true,
                colorMode: 'blackwhite',
                detailLevel: 'high'
            },
            sketch: {
                algorithm: 'sketch',
                threshold: 150,
                smoothness: 6,
                noiseReduction: 1,
                cornerThreshold: 0.7,
                enableOptimization: false,
                colorMode: 'grayscale',
                detailLevel: 'medium'
            },
            photo: {
                algorithm: 'color',
                threshold: 128,
                smoothness: 4,
                noiseReduction: 3,
                cornerThreshold: 0.8,
                enableOptimization: true,
                colorMode: 'color',
                maxColors: 32,
                detailLevel: 'ultra'
            },
            art: {
                algorithm: 'posterize',
                threshold: 120,
                smoothness: 5,
                noiseReduction: 2,
                cornerThreshold: 0.75,
                enableOptimization: true,
                colorMode: 'limited',
                maxColors: 16,
                detailLevel: 'high'
            }
        };

        return presets[presetName] || presets.logo;
    }

    static applyPreset(presetName, controls) {
        const preset = this.getPreset(presetName);
        
        Object.keys(preset).forEach(key => {
            const control = document.getElementById(key);
            if (control) {
                if (control.type === 'range') {
                    control.value = preset[key];
                    control.dispatchEvent(new Event('input'));
                } else if (control.type === 'select-one') {
                    control.value = preset[key];
                    control.dispatchEvent(new Event('change'));
                } else if (control.type === 'checkbox') {
                    control.checked = preset[key];
                    control.dispatchEvent(new Event('change'));
                }
            }
        });
        
        UIEffects.createNotification(`Applied ${presetName} preset`, 'success');
    }
}

// Real-time preview manager
class PreviewManager {
    constructor(containerId) {
        this.container = document.getElementById(containerId);
        this.zoomLevel = 1;
        this.panOffset = { x: 0, y: 0 };
        this.isDragging = false;
        this.lastPanPoint = { x: 0, y: 0 };
        
        this.initializeEvents();
    }

    initializeEvents() {
        // Zoom events
        this.container.addEventListener('wheel', this.handleWheel.bind(this));
        
        // Pan events
        this.container.addEventListener('mousedown', this.startPan.bind(this));
        this.container.addEventListener('mousemove', this.doPan.bind(this));
        this.container.addEventListener('mouseup', this.endPan.bind(this));
        this.container.addEventListener('mouseleave', this.endPan.bind(this));
        
        // Touch events for mobile
        this.container.addEventListener('touchstart', this.handleTouchStart.bind(this));
        this.container.addEventListener('touchmove', this.handleTouchMove.bind(this));
        this.container.addEventListener('touchend', this.handleTouchEnd.bind(this));
    }

    handleWheel(event) {
        event.preventDefault();
        
        const delta = -Math.sign(event.deltaY);
        const zoomFactor = 1.1;
        const newZoom = this.zoomLevel * Math.pow(zoomFactor, delta);
        
        this.setZoom(newZoom, event.offsetX, event.offsetY);
    }

    setZoom(zoom, centerX, centerY) {
        const oldZoom = this.zoomLevel;
        this.zoomLevel = Math.max(0.1, Math.min(5, zoom));
        
        // Adjust pan offset to zoom around center point
        if (centerX !== undefined && centerY !== undefined) {
            const scaleChange = this.zoomLevel / oldZoom;
            this.panOffset.x = centerX - (centerX - this.panOffset.x) * scaleChange;
            this.panOffset.y = centerY - (centerY - this.panOffset.y) * scaleChange;
        }
        
        this.updateTransform();
        this.updateZoomDisplay();
    }

    startPan(event) {
        if (event.button !== 0) return; // Only left mouse button
        
        this.isDragging = true;
        this.lastPanPoint = { x: event.clientX, y: event.clientY };
        this.container.style.cursor = 'grabbing';
    }

    doPan(event) {
        if (!this.isDragging) return;
        
        const dx = event.clientX - this.lastPanPoint.x;
        const dy = event.clientY - this.lastPanPoint.y;
        
        this.panOffset.x += dx;
        this.panOffset.y += dy;
        
        this.lastPanPoint = { x: event.clientX, y: event.clientY };
        this.updateTransform();
    }

    endPan() {
        this.isDragging = false;
        this.container.style.cursor = 'grab';
    }

    handleTouchStart(event) {
        if (event.touches.length === 1) {
            this.startPan({
                clientX: event.touches[0].clientX,
                clientY: event.touches[0].clientY,
                button: 0
            });
        }
    }

    handleTouchMove(event) {
        if (event.touches.length === 1) {
            this.doPan({
                clientX: event.touches[0].clientX,
                clientY: event.touches[0].clientY
            });
        }
    }

    handleTouchEnd() {
        this.endPan();
    }

    updateTransform() {
        const svgElement = this.container.querySelector('svg');
        if (svgElement) {
            svgElement.style.transform = `translate(${this.panOffset.x}px, ${this.panOffset.y}px) scale(${this.zoomLevel})`;
            svgElement.style.transformOrigin = '0 0';
        }
    }

    updateZoomDisplay() {
        const zoomDisplay = document.querySelector('.zoom-level');
        if (zoomDisplay) {
            zoomDisplay.textContent = `${Math.round(this.zoomLevel * 100)}%`;
        }
    }

    resetView() {
        this.zoomLevel = 1;
        this.panOffset = { x: 0, y: 0 };
        this.updateTransform();
        this.updateZoomDisplay();
    }

    fitToContainer() {
        const containerRect = this.container.getBoundingClientRect();
        const svgElement = this.container.querySelector('svg');
        
        if (svgElement) {
            const svgWidth = parseInt(svgElement.getAttribute('width'));
            const svgHeight = parseInt(svgElement.getAttribute('height'));
            
            const scaleX = containerRect.width / svgWidth;
            const scaleY = containerRect.height / svgHeight;
            const scale = Math.min(scaleX, scaleY, 1);
            
            this.setZoom(scale);
            this.panOffset = {
                x: (containerRect.width - svgWidth * scale) / 2,
                y: (containerRect.height - svgHeight * scale) / 2
            };
            
            this.updateTransform();
        }
    }
}

